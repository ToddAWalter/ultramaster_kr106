// Interpolated VCA gain lookup.
// Maps normalized envelope (0..1) to linear VCA gain (0..1) through the
// circuit-derived transfer function.
static inline float VCAGain(float envNorm)
{
  float idx = envNorm * 255.f;
  int i0 = static_cast<int>(idx);
  i0 = std::min(i0, 254);
  float frac = idx - i0;
  return kVCATable[i0] + frac * (kVCATable[i0 + 1] - kVCATable[i0]);
}

